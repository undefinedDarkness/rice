local menubar = require("menubar")
local hotkeys_popup = require("awful.hotkeys_popup")

local date = require('lgi').GLib.DateTime.new_now_local():format("%d-%m-%y")

local function take_screenshot(flags)
  flags = flags or ''
  local path = os.getenv("HOME") .."/Pictures/Screenshots/" .. date .. '-' .. math.random(1, 1000) .. '.png'
	  awful.spawn.easy_async("scrot -b -f "..flags..' '..path, function(stdout, stderr)
		if #stderr > 0 then
		  require('naughty').notify { text = "scrot: " .. stderr }
		else
		  require('naughty').notify { text = "Screenshot Taken.", icon = path }
		end
	  end)

end

